package ejerciciosBasicos1;

public class Ejercicio2 {

		private static void operaciones() {
			int numero1 = 15;
			int numero2 = 4;
			
			System.out.println(numero1+" + "+numero2+" = "+(numero1+numero2));
			System.out.println(numero1+" - "+numero2+" = "+(numero1-numero2));
			System.out.println(numero1+" / "+numero2+" = "+(numero1/numero2));
			System.out.println(numero1+" * "+numero2+" = "+(numero1*numero2));
			System.out.println(numero1+" % "+numero2+" = "+(numero1%numero2));

		}
	
	public static void main(String[] args) {
		operaciones();
	}
	
}
